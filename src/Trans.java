    private void TRANSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TRANSActionPerformed
        String nacc = JOptionPane.showInputDialog(null, "Please input recepient's Account Number");
        int selectedOption = JOptionPane.showConfirmDialog(null,
                "Proceed to cash transfer",
                "CONFRIM ACTION!",
                JOptionPane.YES_NO_OPTION);
        if (selectedOption == JOptionPane.YES_OPTION) {

            String ac = acno.getText();
            String p = pass.getText();
            String bb = amnt.getText();
            if (ac.isEmpty() || p.isEmpty() || bb.isEmpty() || nacc.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Empty Field(s)!\nPlease try again", "NULL INPUT", JOptionPane.PLAIN_MESSAGE);
            } else {
                try {
                    Class.forName("com.mysql.jdbc.Driver");

                    String path = "jdbc:mysql://localhost:3306/transbank";
                    String user = "root";
                    String passw = "";
                    Connection con = DriverManager.getConnection(path, user, passw);
                    Statement stmt = con.createStatement();
                    Statement stmt1 = con.createStatement();
                    Statement stmt2 = con.createStatement();
                    Statement stmt3 = con.createStatement();
                    ResultSet rs;
                    ResultSet rs1;

                    String sql = "select Amount from balance where AccountNo=('" + nacc + "')";
                    rs = stmt.executeQuery(sql);

                    while (rs.next()) {
                        int q = rs.getInt("Amount");
                        int namt = Integer.parseInt(bb);
                        int w = namt + q;

                        String sqls = "update balance set Amount = ('" + w + "') where AccountNo = ('" + nacc + "')";
                        stmt1.execute(sqls);
                        String e = Integer.toString(w);
                        amnt.setText(e);
                    }

                    String que = "select Amount from balance where AccountNo=('" + ac + "')";
                    rs1 = stmt.executeQuery(que);
                    while (rs1.next()) {
                        int s = rs1.getInt("Amount");
                        int namts = Integer.parseInt(bb);
                        int d = s - namts;

                        String query = "update balance set Amount = ('" + d + "') where AccountNo = ('" + ac + "')";
                        stmt3.execute(query);
                        String g = Integer.toString(d);
                        amnt.setText(g);
                    }
                    String sqlss = "insert into transfer(SenderAccNo, Amount, RecepientAccNo) values ('" + ac + "','" + bb + "','" + nacc + "')";
                    stmt2.execute(sqlss);
                    JOptionPane.showMessageDialog(null, "Transaction Successful!\nYou have transfered Ksh " + bb + " to account number " + nacc + "",
                            "TRANSFER", JOptionPane.PLAIN_MESSAGE);

                    Trans tr = new Trans();
                    tr.setVisible(true);
                    dispose();
                } catch (HeadlessException | ClassNotFoundException | SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Error!\n" + ex, "ERROR", JOptionPane.ERROR_MESSAGE);
                    amnt.setText(null);
                    acno.setText(null);
                    pass.setText(null);
                }
            }
        }
    }//GEN-LAST:event_TRANSActionPerformed

