    private void WIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_WIDActionPerformed

        int selectedOption = JOptionPane.showConfirmDialog(null,
                "Do you wish to proceed with withdrawal",
                "CONFRIM ACTION!",
                JOptionPane.YES_NO_OPTION);
        if (selectedOption == JOptionPane.YES_OPTION) {
            String ac = acno.getText();
            String p = pass.getText();
            String aa = amnt.getText();

            try {
                Class.forName("com.mysql.jdbc.Driver");

                String path = "jdbc:mysql://localhost:3306/transbank";
                String user = "root";
                String passw = "";

                Connection con = DriverManager.getConnection(path, user, passw);
                Statement stmt = con.createStatement();
                Statement stmt1 = con.createStatement();
                Statement stmt2 = con.createStatement();
                ResultSet rs;

                if (ac.isEmpty() || p.isEmpty() || aa.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Empty Field(s)\nPlease input correct details!", "NULL INPUT", JOptionPane.PLAIN_MESSAGE);
                } else {
                    String sql = "select amount from balance where AccountNo=('" + ac + "')";
                    rs = stmt.executeQuery(sql);

                    int wida = Integer.parseInt(aa);
                    while (rs.next()) {
                        a = rs.getInt("Amount");
                    }
                    if (wida > a) {
                        JOptionPane.showMessageDialog(null, "Insuficient funds!", "CUSTOMER ACCOUNT", JOptionPane.PLAIN_MESSAGE);
                    } else {

                        int t = a - wida;

                        String query = "update balance set amount =('" + t + "') where AccountNo =('" + ac + "')";
                        stmt1.execute(query);
                        String f = Integer.toString(t);
                        amnt.setText(f);
                        String query1 = "INSERT INTO withdraw(AccountNo,Amount) VALUES ('" + ac + "','" + aa + "') ";
                        stmt2.execute(query1);

                        JOptionPane.showMessageDialog(null, "Withdrawn Ksh " + aa + " Successfully!\nNew Balance is: " + t + "",
                                "CUSTOMER ACCOUNT", JOptionPane.PLAIN_MESSAGE);
                    }
                    Trans tr = new Trans();
                    tr.setVisible(true);
                    dispose();
                }
            } catch (HeadlessException | ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error!\n" + ex, "ERROR", JOptionPane.ERROR_MESSAGE);
                amnt.setText(null);
                acno.setText(null);
                pass.setText(null);
            }
        }
    }//GEN-LAST:event_WIDActionPerformed

